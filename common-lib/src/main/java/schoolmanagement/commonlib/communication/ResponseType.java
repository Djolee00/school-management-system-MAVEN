package schoolmanagement.commonlib.communication;

/**
 * The ResponseType enum represents the type of response.
 *
 * <p>
 * It includes two possible values: SUCCESS and FAILURE.
 * </p>
 * 
 * @see Response
 * @author Djordje Ivanovic
 */
public enum ResponseType {
	/**
	 * Represents a successful response.
	 */
	SUCCESS,

	/**
	 * Represents a failure response.
	 */
	FAILURE
}
