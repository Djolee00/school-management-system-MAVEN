package schoolmanagement.commonlib.model.enums;

/**
 * The Level enum represents language proficiency levels according to the Common European Framework of Reference for Languages (CEFR).
 * It includes six levels: A1, A2, B1, B2, C1, and C2.
 */
public enum Level {
    
    A1,
    A2,
    B1,
    B2,
    C1,
    C2
    
}
